// Tile Block Sets Data - Different visual styles for game tiles
export interface TileBlock {
  id: string;
  name: string;
  description: string;
  price: number;
  preview: string; // emoji or icon
  colors: Record<number, { bg: string; text: string; glow: string }>;
}

// Generate all tile values from 2 to 2^100 (approximately 2.53e30)
const generateTileValues = (): number[] => {
  const values: number[] = [];
  for (let i = 1; i <= 100; i++) {
    values.push(Math.pow(2, i));
  }
  return values;
};

export const allTileValues = generateTileValues();

// Helper to generate diverse colors for a theme
const generateColorPalette = (baseColors: Array<{bg: string, text: string, glow: string}>) => {
  const colors: Record<number, { bg: string; text: string; glow: string }> = {};
  allTileValues.forEach((value, index) => {
    const colorIndex = index % baseColors.length;
    colors[value] = baseColors[colorIndex];
  });
  return colors;
};

